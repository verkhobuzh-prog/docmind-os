STYLE_INSTRUCTIONS = {
    "concise":   "Відповідай стисло — максимум 3-5 речень.",
    "balanced":  "Давай збалансовані відповіді — достатньо деталей без зайвого.",
    "detailed":  "Давай розгорнуті, детальні відповіді з усіма нюансами.",
}


def build_system_prompt(profile: ProfileRead | None) -> str:
    if profile is None:
        return (
            "Ти AI-помічник для роботи з документами DocMind OS. "
            "Відповідай українською мовою. "
            "Посилайся лише на надані документи. "
            "Якщо відповідь відсутня в документах — скажи про це."
        )

    complexity   = COMPLEXITY_INSTRUCTIONS.get(profile.complexity_level, COMPLEXITY_INSTRUCTIONS[3])
    domain       = DOMAIN_INSTRUCTIONS.get(profile.domain, DOMAIN_INSTRUCTIONS["general"])
    style        = STYLE_INSTRUCTIONS.get(
        profile.preferences.response_style, STYLE_INSTRUCTIONS["balanced"]
    )
    lang         = "Відповідай українською мовою." if profile.preferences.language == "uk" else \
                   "Respond in English."

    forbidden    = ""
    if profile.preferences.forbidden_topics:
        topics = ", ".join(profile.preferences.forbidden_topics)
        forbidden = f"\nНЕ відповідай на питання про: {topics}."

    return (
        f"Ти AI-помічник DocMind OS. Профіль: '{profile.name}'.\n\n"
        f"РІВЕНЬ СКЛАДНОСТІ ({profile.complexity_level}/5): {complexity}\n\n"
        f"ГАЛУЗЬ ({profile.domain}): {domain}\n\n"
        f"СТИЛЬ ВІДПОВІДІ: {style}\n\n"
        f"{lang}{forbidden}\n\n"
        "ВАЖЛИВО: Посилайся ЛИШЕ на надані документи. "
        "Якщо відповідь відсутня в документах — скажи: "
        "'Ця інформація відсутня в наданих документах.'"
    )
